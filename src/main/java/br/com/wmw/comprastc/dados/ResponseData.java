package br.com.wmw.comprastc.dados;

public class ResponseData {
	
	   	Args args;
	    String origin;
	    String url;

	    public Args getArgs() {
	        return args;
	    }

	    public void setArgs(Args args) {
	        this.args = args;
	    }

	    public String getOrigin() {
	        return origin;
	    }

	    public void setOrigin(String origin) {
	        this.origin = origin;
	    }

	    public String getUrl() {
	        return url;
	    }

	    public void setUrl(String url) {
	        this.url = url;
	    }
	

}
